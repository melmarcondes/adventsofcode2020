var maxSeatId = 0;
for(var i = 0; i < codes.length; i++){
    var rowF = 0;
    var rowB = 127;
    var colL = 0;
    var colR = 7;
    var row, col, seatId = 0;
    var code = codes[i];
    //F B F B B F F   R L R
    for(var j = 0; j < 10; j++){
        var char =  code.charAt(j)
        if(j <= 6){
            if(char == 'F'){
                rowB = parseInt(rowF + ((rowB - rowF)/2)); //0 - (0 + ((128-0)/2)))
                rowF = rowF
            }
            if(char == 'B'){
                rowB = rowB
                rowF = Math.ceil(((rowB - rowF) / 2) + rowF);
            }
            if(j == 6){
                row = (char == 'F') ? rowF : rowB;               
            }
             //0 - 64, 32 - 64, 32 - 48, 40 - 48, 44 - 48, 44 - 46, 44 - 45 
        }
        if(j > 6){
            if(char == 'L'){
                colL = colL
                colR = parseInt(colL + ((colR - colL)/2));
            }
            if(char == 'R'){
                colL = Math.ceil(((colR - colL) / 2) + colL);
                colR = colR
            }
            if(j == 9){
                col = (char == 'L') ? colL : colR; 
            }
            //3 - 7, 3 - 5, 4 - 5
        }
    }
    seatId = row * 8 + col;
    if(seatId > maxSeatId){
        maxSeatId = seatId;
    }
}
